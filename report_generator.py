def format_report(topic, content):
    report = f"""
=========================================
AUTONOMOUS RESEARCH REPORT
=========================================

Title:
{topic}

Introduction:
This report explores the topic in detail.

Key Findings:
{content}

Challenges:
- Data accuracy
- Limited sources
- Rapid changes

Future Scope:
- More automation
- Better intelligence

Conclusion:
This topic has strong future impact.
"""
    return report